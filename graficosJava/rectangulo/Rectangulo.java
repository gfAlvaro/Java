package rectangulo;

public class Rectangulo{
	
	public static double area( double base, double altura ){
		return base * altura;
		}

	public static double perimetro( double base, double altura ){
		return 2 * ( base + altura );
		}
	
	} // Fin de la clase Rectangulo
